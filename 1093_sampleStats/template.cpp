class Solution
{
public:
    vector<double> sampleStats(vector<int> &count)
    {
        map<int, int> mode_dir;
        double num_min = 0, num_max = 0, avg = 0, mid = 0, mode = 0;
        int num_size = 0;
        int mode_counter = 0;

        for (size_t i = 0; i < count.size(); i++)
        {
            if (count[i] != 0)
                mode_dir[i] = count[i];
            num_size += count[i];
        }

        auto iter = mode_dir.begin();
        num_min = (*iter).first;
        iter = --mode_dir.end();
        num_max = (*iter).first;

        for (auto &kv : mode_dir)
        {
            if (mode_counter < kv.second)
            {
                mode = kv.first;
                mode_counter = kv.second;
            }

            avg += ((double)kv.first * (double)kv.second) / (double)num_size;
        }

        auto mid_pos = num_size / 2;
        int mid_counter = 0;
        if (num_size % 2 > 0)
        {
            for (auto &kv : mode_dir)
            {
                if ((mid_counter) <= mid_pos && (mid_counter + kv.second) > mid_pos)
                {
                    mid = kv.first;
                    break;
                }
                else
                {
                    mid_counter += kv.second;
                }
            }
        }
        else
        {
            for (auto &kv : mode_dir)
            {
                if ((mid_counter) >= mid_pos)
                { 
                    if( (mid_pos+1) > (mid_counter ))
                    {
                        mid = (kv.first + mid) / 2.;
                    } 
                    break;
                }
                else
                {
                    mid_counter += kv.second;
                    mid = kv.first;
                }
            }
        }

        return vector<double>{num_min, num_max, avg, mid, mode};
    }
};

